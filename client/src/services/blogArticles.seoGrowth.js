function makeGrowthArticle(config) {
    const fallbackCoverPool = [
        'youtube-channel-audit-checklist-2026.svg',
        'community-posts-growth-system.svg',
        'seo-keywords-for-youtube-channel-pages.svg',
        'subscriber-quality-vs-volume-in-exchanges.svg',
        'youtube-collab-kpi-dashboard-2026.svg',
        'youtube-shorts-to-long-videos-funnel.svg',
        'content-calendar-with-analytics-loop.svg',
        'local-ukrainian-youtube-niches-2026.svg',
    ];
    const coverIndex = Array.from(config.slug).reduce((sum, char) => sum + char.charCodeAt(0), 0) % fallbackCoverPool.length;

    return {
        slug: config.slug,
        title: config.title,
        excerpt: config.excerpt,
        coverImage: config.coverImage || `/images/blog/${fallbackCoverPool[coverIndex]}`,
        coverAlt: config.coverAlt,
        publishedAt: config.publishedAt,
        publishedAtIso: config.publishedAtIso,
        readTime: config.readTime || '11 хв читання',
        tags: config.tags,
        seoTitle: config.seoTitle,
        seoDescription: config.seoDescription,
        keywords: config.keywords,
        relatedSlugs: config.relatedSlugs || [],
        chart: config.chart,
        faq: config.faq,
        sections: makeGrowthSections(config.sections),
    };
}

